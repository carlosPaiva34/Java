    private double calcularValorPedido() {
        int linha = jTtabela.getRowCount();
        double total = 0;
        for (int i = 0; i < linha; i++) {
            total += Double.parseDouble(String.valueOf(jTtabela.getValueAt(i, 4)));
        }
        return total;
    }//fim

