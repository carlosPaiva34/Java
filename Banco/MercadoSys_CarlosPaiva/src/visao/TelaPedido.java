    private void fecharPedido() {
        Usuario u = new Usuario();
        // UsuarioDao dao =new UsuarioDao();
        u.setId(UsuarioDao.usuario.getId());
        Cliente c = new Cliente();
        c.setCpf(jFcpf.getText().replaceAll("[.-]", ""));
        Pedido p = new Pedido();
        p.setCliente(c);
        p.setUsuario(u);
        p.setValor(Double.parseDouble(jTtotal.getText()));
        p.setDtEntrega(jDateChooser1.getDate());
        try {
            new PedidoDao().salvar(p);
            JOptionPane.showMessageDialog(null, "Pedido Salvo com sucesso !");
        } catch (SQLException ex) {
            System.out.println();
            JOptionPane.showMessageDialog(null, "Erro ao salvar " + ex.getMessage());
        }
    }//im
